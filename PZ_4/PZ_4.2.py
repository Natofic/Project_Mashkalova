"""
Дано число A (>1). Вывести наименьшее из целых чисел K,
для которых сумма 1 + 1/2 + ... + 1/K будет больше A, и саму эту сумму.
"""

try:
    A = int(input("Введите число A (>1): "))

    if A > 1:
        total = 0.0
        K = 0

        while total <= A:
            K += 1
            total += 1 / K

        print(f"Наименьшее K = {K}")
        print(f"Сумма 1 + 1/2 + ... + 1/{K} = {total:.6f}")
    else:
        print("Ошибка: Число A должно быть больше 1")

except ValueError:
    print("Ошибка: Введите целое положительное число!")